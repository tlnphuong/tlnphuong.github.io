CREATE SCHEMA doordash;
USE doordash;
DROP TABLE IF EXISTS OriginalDeliveryData;
CREATE TABLE OriginalDeliveryData(
	DELIVERY_UUID VARCHAR(100) NOT NULL,
	DELIV_CREATED_AT DATETIME NOT NULL,
	DELIV_STORE_NAME VARCHAR(20) NOT NULL,
	DELIV_DASHER_ID INT,
	DELIV_SUBMARKET VARCHAR(50),
	DELIV_D2R FLOAT,
	DELIV_IS_20_MIN_LATE BOOLEAN,
	DELIV_CLAT FLOAT,
	DELIV_CANCELLED_AT DATETIME,
	DELIV_MISSING_INCORRECT_REPORT BOOLEAN,
	WAS_REQUESTED INT,
	WAS_MISSING INT,
	WAS_SUBBED INT,
	WAS_FOUND INT,
	ITEM_NAME TEXT, 
	ITEM_PRICE FLOAT,
	ITEM_CATEGORY VARCHAR(50),
	SUBSTITUTE_ITEM_NAME TEXT,
	SUBSTITUTE_ITEM_CATEGORY VARCHAR(50),
    DELIV_CANCELLED BOOLEAN);
DROP TABLE IF EXISTS DeliveryData;
CREATE TABLE DeliveryData(
	DELIVERY_UUID VARCHAR(100) NOT NULL,
	DELIV_CREATED_AT DATETIME NOT NULL,
	DELIV_STORE_NAME VARCHAR(20) NOT NULL,
	DELIV_DASHER_ID INT,
	DELIV_SUBMARKET VARCHAR(50),
	DELIV_D2R FLOAT,
	DELIV_IS_20_MIN_LATE BOOLEAN,
	DELIV_CLAT FLOAT,
	DELIV_CANCELLED_AT DATETIME,
	DELIV_MISSING_INCORRECT_REPORT BOOLEAN,
	WAS_REQUESTED INT,
	WAS_MISSING INT,
	WAS_SUBBED INT,
	WAS_FOUND INT,
	ITEM_NAME TEXT, 
	ITEM_PRICE FLOAT,
	ITEM_CATEGORY VARCHAR(50),
	SUBSTITUTE_ITEM_NAME TEXT,
	SUBSTITUTE_ITEM_CATEGORY VARCHAR(50),
    DELIV_CANCELLED BOOLEAN);
